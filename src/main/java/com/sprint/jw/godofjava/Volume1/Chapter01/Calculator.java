package com.sprint.jw.godofjava.Volume1.Chapter01;

public class Calculator {

  public int add(int a, int b) {
    int result = a + b;
    return result;
  }

  public int addAll(int a, int b, int c) {
    int result = a + b + c;
    return result;
  }

  public int subtract(int a, int b) {
    int result = a - b;
    return result;
  }

  public int multiply(int a, int b) {
    int result = a * b;
    return result;
  }

  public int divide(int a, int b) {
    int result = a / b;
    return result;
  }
}
